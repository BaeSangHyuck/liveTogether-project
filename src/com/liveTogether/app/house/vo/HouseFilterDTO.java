package com.liveTogether.app.house.vo;

public class HouseFilterDTO {
	private int min;
	private int max;
	private String houseGender; //mul 
	private String houseType;  //mul
	private String roomType;  //mul
	private String roomDate;
	
	public HouseFilterDTO() {;}

	public int getMin() {
		return min;
	}

	public void setMin(int min) {
		this.min = min;
	}

	public int getMax() {
		return max;
	}

	public void setMax(int max) {
		this.max = max;
	}

	public String getHouseGender() {
		return houseGender;
	}

	public void setHouseGender(String houseGender) {
		this.houseGender = houseGender;
	}

	public String getHouseType() {
		return houseType;
	}

	public void setHouseType(String houseType) {
		this.houseType = houseType;
	}

	public String getRoomType() {
		return roomType;
	}

	public void setRoomType(String roomType) {
		this.roomType = roomType;
	}

	public String getRoomDate() {
		return roomDate;
	}

	public void setRoomDate(String roomDate) {
		this.roomDate = roomDate;
	}
	
	public String[] getGenderArr() {
		return houseGender.split("");
	}
	
	public String[] getHouseArr() {
		return houseType.split("");
	}
	
	public String[] getRoomArr() {
		return roomType.split("");
	}
	
}
